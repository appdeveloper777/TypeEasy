import polars as pl, time
t0 = time.perf_counter()
df = pl.read_csv("/code/productos_10000000.csv")
t1 = time.perf_counter()
s = df["precio"].sum()
t2 = time.perf_counter()
c1 = (df["precio"] > 500).sum()
t3 = time.perf_counter()
c2 = (df["precio"] < 100).sum()
t4 = time.perf_counter()
c3 = (df["precio"] == 2).sum()
t5 = time.perf_counter()
print(f"filas={len(df)}")
print(f"sumBy.precio={s}")
print(f"countWhere>500={c1}")
print(f"countWhere<100={c2}")
print(f"countWhere==2={c3}")
print(f"load_ms={(t1-t0)*1000:.1f} sum_ms={(t2-t1)*1000:.1f} c1_ms={(t3-t2)*1000:.1f} c2_ms={(t4-t3)*1000:.1f} c3_ms={(t5-t4)*1000:.1f} total_ms={(t5-t0)*1000:.1f}")
